export const useTranslation = (user) => {
  const lang = user?.languagePreference || 'English';
  
  return (key, params = {}) => {
    // Return localized string if exists, fallback to English, then bare key
    let str = (translations[lang] && translations[lang][key]) 
              || translations['English'][key] 
              || key;
              
    // Simple interpolation for params
    Object.keys(params).forEach(p => {
       str = str.replace(`{${p}}`, params[p]);
    });
    
    return str;
  };
};
